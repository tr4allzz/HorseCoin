// Helper function to get race by ID
export const getRaceById = (id) => {
  return sundayRaceData.find(race => race.id === id);
};

// Helper function to get all horses
export const getAllHorses = () => {
  return sundayRaceData.flatMap(race => race.horses);
};

// Helper function to get race statistics
export const getRaceStats = () => {
  const totalRaces = sundayRaceData.length;
  const totalHorses = getAllHorses().length;
  const totalPrize = sundayRaceData.reduce((sum, race) => {
    const prizeAmount = parseInt(race.prize.replace(/[^\d]/g, ''));
    return sum + prizeAmount;
  }, 0);

  return {
    totalRaces,
    totalHorses,
    totalPrize: `${totalPrize.toLocaleString()} PLN`
  };
};